function adfoxGetCookie(name) {
    var pos = document.cookie.indexOf(name + "=");
    if (pos == -1) {
        return null;
    } else {
        var pos2 = document.cookie.indexOf(";", pos);
        if (pos2 == -1) {
            return unescape(document.cookie.substring(pos + name.length + 1));
        } else {
            return unescape(document.cookie.substring(pos + name.length + 1, pos2));
        }
    }
}

function adfoxWriteCookie(name, value, expires, path, domain, secure) {
    document.cookie =
        name +"=" + escape(value) +
            ((expires) ? "; expires=" + expires.toGMTString() : "") +
            ((path) ? "; path=" + path : "") +
            ((domain) ? "; domain=" + domain : "") +
            ((secure) ? "; secure" : "");
}

